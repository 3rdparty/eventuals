#pragma once

#include "stout/eventual.h"

////////////////////////////////////////////////////////////////////////

namespace stout {
namespace eventuals {

////////////////////////////////////////////////////////////////////////

template <typename T>
auto Just(T t) {
  return Eventual<T>([t = std::move(t)](auto& k) {
    eventuals::succeed(k, std::move(t));
  });
}

inline auto Just() {
  return Eventual<void>([](auto& k) {
    eventuals::succeed(k);
  });
}

////////////////////////////////////////////////////////////////////////

} // namespace eventuals
} // namespace stout

